from colorama import Fore

def dark_blue(string):
    return Fore.BLUE + string + Fore.WHITE

def dark_yellow(string):
    return Fore.YELLOW + string + Fore.WHITE

def dark_cyan(string):
    return Fore.CYAN + string + Fore.WHITE

def dark_green(string):
    return Fore.GREEN + string + Fore.WHITE

def magenta(string):
    return Fore.MAGENTA + string + Fore.WHITE

def dark_red(string):
    return Fore.RED + string + Fore.WHITE

def charcoal(string):
    return Fore.LIGHTBLACK_EX + string + Fore.WHITE

def blue(string):
    return Fore.LIGHTBLUE_EX + string + Fore.WHITE

def cyan(string):
    return Fore.LIGHTCYAN_EX + string + Fore.WHITE

def green(string):
    return Fore.LIGHTGREEN_EX + string + Fore.WHITE

def pink(string):
    return Fore.LIGHTMAGENTA_EX + string + Fore.WHITE

def red(string):
    return Fore.LIGHTRED_EX + string + Fore.WHITE

def yellow(string):
    return Fore.LIGHTYELLOW_EX + string + Fore.WHITE
